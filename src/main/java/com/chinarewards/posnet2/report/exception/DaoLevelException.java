package com.chinarewards.posnet2.report.exception;

public class DaoLevelException extends Exception {

	private static final long serialVersionUID = -9191668766860119680L;

	public DaoLevelException() {
		super();
	}

	public DaoLevelException(String message, Throwable cause) {
		super(message, cause);
	}

	public DaoLevelException(String message) {
		super(message);
	}

	public DaoLevelException(Throwable cause) {
		super(cause);
	}

}
