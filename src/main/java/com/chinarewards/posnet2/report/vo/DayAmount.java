package com.chinarewards.posnet2.report.vo;


public class DayAmount {
	
	private String time;
	private Double amount;
	
	public DayAmount() {
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public Double getAmount() {
		return amount;
	}

	public void setAmount(Double amount) {
		this.amount = amount;
	}
	
	
}
