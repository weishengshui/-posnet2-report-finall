package com.chinarewards.posnet2.report.vo;


public class DayCount {
	
	private String time;
	private Integer count;
	public DayCount() {
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	
}
